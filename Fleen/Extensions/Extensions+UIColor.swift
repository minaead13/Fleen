import UIKit


extension UIColor {
    
    class var orangePeel: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 160.0 / 255.0, blue: 0.0 / 255.0, alpha: 1.0)
    }
    
    class var placeHolder : UIColor {
        return UIColor(red: 194/255, green: 194/255, blue: 194/255, alpha: 1)
    }
    
    class var gold : UIColor{
        UIColor(red: CGFloat(35) / 255.0,green: CGFloat(170) / 255.0,blue: CGFloat(73) / 255.0, alpha: 1.0)
    }
}
